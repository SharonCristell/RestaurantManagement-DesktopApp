    private void jButtonVerPlatosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonVerPlatosActionPerformed

        try {

            ArrayList<Pedido> listita;

            listita = listaMozo.obtenerPedidos(nodito);

            Plato[] lista = new Plato[4];

            Plato p;
            //lista=listaMozo.obtenerPlatos(listita, ptr);
            String[] cad = new String[4];

            Pedido refp = listita.get(jComboBoxVerPedidos.getSelectedIndex());
            //lista=refp.getListaPlatos();
            String[] titulo = {"NumeroPedido", "Descripcion", "Cantidad", "PrecioUnitario"};
            modelo = new DefaultTableModel(null, titulo);
            for (int i = 0; i < lista.length; i++) {

                p = refp.getListaPlatos()[i];
                cad[0] = String.valueOf(i + 1);

                cad[1] = p.getDescripcion();
                cad[2] = String.valueOf(p.getCantidad());
                cad[3] = String.valueOf(p.getPrecioUnitario());

                modelo.addRow(cad);
                jTableVerPedidoXMozo.setModel(modelo);

            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "No hay datos asociados");
        }


    }//GEN-LAST:event_jButtonVerPlatosActionPerformed

