    private void AsignarMozoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AsignarMozoActionPerformed
        pedido = new Pedido();
        ArrayList<Plato> platos = new ArrayList<>();

        if (BoxBrasa1.isSelected()) {
            Plato brasa = new Plato("Pollo a la Brasa");
            platos.add(brasa);

        }
        if (BoxCausa.isSelected()) {
            Plato causa = new Plato("Causa");
            platos.add(causa);
        }
        if (BoxCeviche.isSelected()) {
            Plato ceviche = new Plato("Ceviche");
            platos.add(ceviche);
        }
        if (BoxEnrollado.isSelected()) {
            Plato enrollado = new Plato("Pollo enrollado");
            platos.add(enrollado);
        }
        if (BoxHuancaina.isSelected()) {
            Plato huancaina = new Plato("Papa a la huancaina");
            platos.add(huancaina);
        }
        if (BoxLomo.isSelected()) {
            Plato lomo = new Plato("Lomos saltado");
            platos.add(lomo);
        }
        if (BoxPescado.isSelected()) {
            Plato pescado = new Plato("Chicharron de pescado");
            platos.add(pescado);
        }
        if (BoxPollo.isSelected()) {
            Plato pescado = new Plato("Arroz con pollo");
            platos.add(pescado);
        }
        if (BoxRellena.isSelected()) {
            Plato rellena = new Plato("Papa rellena");
            platos.add(rellena);

        }
        if (platos.size() == 4) {

            BoxRellena.setSelected(false);
            BoxBrasa1.setSelected(false);
            BoxCausa.setSelected(false);
            BoxCeviche.setSelected(false);
            BoxEnrollado.setSelected(false);
            BoxHuancaina.setSelected(false);
            BoxLomo.setSelected(false);
            BoxPescado.setSelected(false);
            BoxPollo.setSelected(false);

            for (Plato item : platos) {
                item.calcularCantidad();
                item.asignarPrecio();
                pedido.agregarPlato(item);

            }
            mocito = listaMozo.seleccionarMozoAleatorio().getDato();
            while (mocito.getColaPedidos().colaLlena() == true) {
                mocito = listaMozo.seleccionarMozoAleatorio().getDato();
            }
            jTextPaneMozo.setText(mocito.getApellidos());
            jTextAreaPedido.setText("");
            pedido.calcularMontoPago();
            jTextAreaPedido.setText(pedido.toString());
            ElegirMozo.setVisible(true);
            ElegirMozo.setLocationRelativeTo(null);
            jFramePedido.dispose();
        } else {
            JOptionPane.showMessageDialog(null, "Debe elegir 4 platos", "Lista Llena", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_AsignarMozoActionPerformed

