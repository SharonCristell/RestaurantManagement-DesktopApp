    private void jButtonVerMontoRecaudacionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonVerMontoRecaudacionActionPerformed
        jTextAreaListadoMontoRec.setText("");
        NodoLEG<Mozo> ptr = listaMozo.getPrimero();
        if (ptr != null) {

            while (ptr != null) {
                double m = listaMozo.calcularMontoRecaudacion(ptr);

                jTextAreaListadoMontoRec.append("\nApellido: " + ptr.getDato().getApellidos()
                        + "\n    Año de experiencia: " + ptr.getDato().getAñosExperiencia()
                        + "\n    Monto recaudado: " + m + "\n");
                ptr = ptr.getSiguiente();
            }

        } else {
            JOptionPane.showMessageDialog(null, "No hay mozos registrados");
        }
    }//GEN-LAST:event_jButtonVerMontoRecaudacionActionPerformed

