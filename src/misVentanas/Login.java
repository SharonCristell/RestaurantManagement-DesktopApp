    private void jButtonLoginEntrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonLoginEntrarActionPerformed
        //validar los datos de usuario
        String username = jTextFieldUsuario.getText();
        String password = String.valueOf(jPasswordFieldPassword.getPassword());
        String nombre="";
        boolean seLogeo = false;
        for (String[] cuenta : cuentas) {
            if (cuenta[0].equals(username) && cuenta[1].equals(password)) {
                seLogeo = true;
                nombre=cuenta[0];
                break;
            }
        }
        if (seLogeo) {
            jLabelLoginError.setText("Usuario y/o Contrase침a Err칩neos");
            
            VentanaPrincipal vp= new VentanaPrincipal(nombre);
            vp.setVisible(true);
            this.dispose();
        } else {
            jLabelLoginError.setText("Nombre de usuario o contrase침a incorrectos");
        }
    }//GEN-LAST:event_jButtonLoginEntrarActionPerformed

